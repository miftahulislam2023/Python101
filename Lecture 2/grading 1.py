"""
A grading system I learnt in lecture 2
"""
x = float(input('Enter your number in Mathematics: '))
if x >= 80:
    print("A+")
elif x >= 70:
    print("A")
elif x >= 60:
    print("A-")
elif x >= 50:
    print("B")

""" > - graeter than
< - less than
>= - greater or equal
<= - less or equal
== - equal """