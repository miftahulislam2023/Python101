"""What are Modules?
Modules are files containing Python code (functions, classes, or variables)
that you can import and use in other Python files.

Example: The math module in Python has many math-related functions and constants like sqrt() and pi.

How to Import Modules:
There are a few ways to import modules-

1. Basic Import: import math
2. Import Specific Items: from math import pi, sqrt
3. Renaming Imports: import math as m (then you can call m.pi, m.sqrt, etc.)"""
